package day08;

public class Pet { // class start
    //name, species, age 멤버 변수
    String name;
    String species;
    int age;

    //name, species, age를 매개변수로 받아 초기화하는 생성자
    Pet( String name , String species , int age ){
        this.name = name;
        this.species = species;
        this.age = age;
    }


} // class end
